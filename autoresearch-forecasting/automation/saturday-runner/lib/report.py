def send_email(subject: str, body: str, attachments: list[Path], cfg: dict) -> bool:
    if not cfg["output"]["email_enabled"]:
        return False
    host = os.environ.get("SMTP_HOST")
    port = int(os.environ.get("SMTP_PORT", "587"))
    user = os.environ.get("SMTP_USER")
    pw = os.environ.get("SMTP_PASS")
    to = os.environ.get("EMAIL_TO")
    if not all([host, user, pw, to]):
        print("  email_enabled=true but env vars missing (SMTP_HOST/SMTP_USER/SMTP_PASS/EMAIL_TO). Skipping.")
        return False
    msg = EmailMessage()
    prefix = cfg["output"].get("email_subject_prefix", "")
    msg["Subject"] = f"{prefix} {subject}".strip()
    msg["From"] = user
    msg["To"] = to
    msg.set_content(body)
    for ap in attachments:
        if ap.exists():
            data = ap.read_bytes()
            msg.add_attachment(
                data,
                maintype="text",
                subtype="markdown" if ap.suffix == ".md" else "csv",
                filename=ap.name,
            )
    with smtplib.SMTP(host, port) as s:
        s.starttls()
        s.login(user, pw)
        s.send_message(msg)
    return True
