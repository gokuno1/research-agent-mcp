def write_markdown(
    out_path: Path,
    market: MarketState,
    ranked: list[StockMetrics],
    selected: list[StockMetrics],
    positions: list[Position],
    capital_inr: float,
    deployed_inr: float,
    cfg: dict,
):
    lines: list[str] = []
    today = datetime.now().strftime("%Y-%m-%d %H:%M IST")
    lines.append(f"# Weekly portfolio — {today}")
    lines.append("")
    lines.append(
        "Generated by `automation/saturday-runner/run.py` per the playbook in "
        "`docs/research-runs/2026-05-02-systematic-swing-trading-evidence-based-playbook.md`."
    )
    lines.append("")
    lines.append(f"**Capital:** {_fmt_inr(capital_inr)}     **Universe:** NIFTY 50     "
                 f"**Holdings:** {len(positions)} / {cfg['portfolio']['target_positions']}")
    lines.append("")
    lines.append("## Step 1 — Market state")
    lines.append("")
    lines.append(f"- NIFTY close: **{market.nifty_close:,.2f}**")
    lines.append(f"- NIFTY 40-week SMA: **{market.nifty_40w_sma:,.2f}** → "
                 f"**{'TREND-ON' if market.trend_on else 'TREND-OFF'}**")
    lines.append(f"- India VIX: **{market.india_vix:.2f}**")
    lines.append(f"- Vol scaling: **×{market.vol_scaling_factor:.2f}** "
                 f"(effective target {market.target_portfolio_vol_pct:.1f}%)")
    for n in market.notes:
        lines.append(f"- ⚠ {n}")
    lines.append("")
    lines.append("## Steps 2–4 — Top-12 candidates (quality-passing, momentum-ranked, PEAD-overlaid)")
    lines.append("")
    lines.append("| # | Symbol | Sector | Mom 12-1 | Vol(ann) | ATR% | Quality | PEAD note |")
    lines.append("|---|---|---|---:|---:|---:|---:|---|")
    for i, m in enumerate(ranked[:12], start=1):
        lines.append(
            f"| {i} | `{m.symbol}` | {m.sector} | "
            f"{m.momentum_12_1*100:+.1f}% | {m.realised_vol_4w_annual*100:.1f}% | "
            f"{m.atr_pct_14d:.2f}% | {m.quality_score}/3 | {m.pead_note or '-'} |"
        )
    lines.append("")
    lines.append("## Steps 5–7 — Final portfolio")
    lines.append("")
    if not positions:
        lines.append("⚠ **NO POSITIONS PASSED ALL FILTERS.** Hold cash this week.")
    else:
        lines.append(
            "| Symbol | Sector | Shares | Entry | Stop | Stop% | Notional | Wt% | Risk | Exit by | Notes |"
        )
        lines.append(
            "|---|---|---:|---:|---:|---:|---:|---:|---:|---|---|"
        )
        for p in positions:
            stop_pct = (p.last_close - p.final_stop_price) / p.last_close * 100.0
            note_text = "; ".join(p.notes) if p.notes else "-"
            lines.append(
                f"| `{p.symbol}` | {p.sector} | {p.shares} | "
                f"₹{p.last_close:,.2f} | ₹{p.final_stop_price:,.2f} | {stop_pct:.2f}% | "
                f"{_fmt_inr(p.notional_inr)} | {p.realised_weight_pct:.1f}% | "
                f"{_fmt_inr(p.risk_inr)} | {p.target_exit_date} | {note_text} |"
            )
        total_risk = sum(p.risk_inr for p in positions)
        cash = capital_inr - deployed_inr
        lines.append("")
        lines.append(f"**Deployed:** {_fmt_inr(deployed_inr)} ({deployed_inr/capital_inr*100:.1f}%)     "
                     f"**Cash buffer:** {_fmt_inr(cash)} ({cash/capital_inr*100:.1f}%)     "
                     f"**Total at-risk:** {_fmt_inr(total_risk)} ({total_risk/capital_inr*100:.2f}%)")

    skipped = _stocks_skipped_for_sizing(selected, positions)
    if skipped:
        lines.append("")
        lines.append("### Selected but skipped at sizing step")
        lines.append("")
        lines.append("| Symbol | Last close | Reason |")
        lines.append("|---|---:|---|")
        for s in skipped:
            note = "; ".join(s.notes) if s.notes else "(unknown)"
            lines.append(f"| `{s.symbol}` | ₹{s.last_close:,.0f} | {note} |")

    lines.append("")
    lines.append("## Step 8 — Execution checklist")
    lines.append("")
    lines.append("- [ ] Compare to last week's holdings (kept what's still in top-12, exit what fell out)")
    lines.append("- [ ] Place buy orders Monday at 09:30 (limit at last close + 0.30%)")
    lines.append("- [ ] Upload `gtt_orders.csv` to your broker for stop-loss GTT orders")
    lines.append("- [ ] Set calendar reminders for `Exit by` dates (time stops)")
    lines.append("- [ ] Halve / exit any held name with earnings within 3 trading days")
    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("Realistic monthly net return band per playbook §8.4: **+0.6% to +1.1%**.")
    lines.append("Worst-month drawdown expectation: **−4% to −8%**. "
                 "Expect a 4-loss streak ~5× per year at 60% win rate. **Do not deviate.**")
    out_path.write_text("\n".join(lines))


