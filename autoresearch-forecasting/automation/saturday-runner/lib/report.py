def _stocks_skipped_for_sizing(selected: list[StockMetrics], positions: list[Position]) -> list[StockMetrics]:
    held_symbols = {p.symbol for p in positions}
    return [s for s in selected if s.symbol not in held_symbols]


def print_console(
    market: MarketState,
    ranked: list[StockMetrics],
    selected: list[StockMetrics],
    positions: list[Position],
    capital_inr: float,
    deployed_inr: float,
    cfg: dict,
):
    print()
    print("=" * 78)
    print(f"  SATURDAY RUNNER — {datetime.now().strftime('%Y-%m-%d %H:%M IST')}")
    print(f"  Capital: {_fmt_inr(capital_inr)}    Universe: NIFTY 50")
    print("=" * 78)

    print()
    print("STEP 1 — Market state")
    print(f"  NIFTY close:          {market.nifty_close:,.2f}")
    print(
        f"  NIFTY 40-WMA:         {market.nifty_40w_sma:,.2f}    "
        f"→ {'TREND-ON' if market.trend_on else 'TREND-OFF'}"
    )
    print(f"  India VIX:            {market.india_vix:.2f}")
    print(
        f"  Vol scaling:          ×{market.vol_scaling_factor:.2f}  "
        f"(effective target = {market.target_portfolio_vol_pct:.1f}%)"
    )
    for n in market.notes:
        print(f"  ! {n}")

    print()
    print("STEPS 2–4 — Quality-passing top-12 momentum candidates")
    print(
        f"  {'Rank':>4}  {'Symbol':<12}  {'Sector':<22}  {'Mom12-1':>8}  "
        f"{'Vol(ann)':>9}  {'ATR%':>6}  {'Q':>2}  PEAD"
    )
    for i, m in enumerate(ranked[:12], start=1):
        pead = m.pead_note or "-"
        print(
            f"  {i:>4}  {m.symbol:<12}  {m.sector:<22}  "
            f"{m.momentum_12_1*100:>7.1f}%  {m.realised_vol_4w_annual*100:>8.1f}%  "
            f"{m.atr_pct_14d:>5.2f}%  {m.quality_score:>2}  {pead}"
        )

    print()
    print(f"STEPS 5–7 — Final portfolio ({len(positions)} of {cfg['portfolio']['target_positions']} target)")
    if not positions:
        print("  ⚠  NO POSITIONS PASSED. Hold cash this week.")
    else:
        print(
            f"  {'Symbol':<12}  {'Sector':<22}  {'Sh':>4}  {'Entry':>9}  "
            f"{'Stop':>9}  {'Stop%':>6}  {'Notional':>10}  {'Wt%':>5}  {'Risk':>7}"
        )
        for p in positions:
            stop_pct = (p.last_close - p.final_stop_price) / p.last_close * 100.0
            print(
                f"  {p.symbol:<12}  {p.sector:<22}  {p.shares:>4}  "
                f"{p.last_close:>9,.2f}  {p.final_stop_price:>9,.2f}  "
                f"{stop_pct:>5.2f}%  {_fmt_inr(p.notional_inr):>10}  "
                f"{p.realised_weight_pct:>4.1f}%  {_fmt_inr(p.risk_inr):>7}"
            )
        total_risk = sum(p.risk_inr for p in positions)
        print(
            f"  {'TOTAL':<12}  {'':22}  {'':>4}  {'':>9}  {'':>9}  {'':>6}  "
            f"{_fmt_inr(deployed_inr):>10}  "
            f"{deployed_inr/capital_inr*100:>4.1f}%  {_fmt_inr(total_risk):>7}"
        )
        cash = capital_inr - deployed_inr
        print(
            f"  Cash buffer: {_fmt_inr(cash)} ({cash/capital_inr*100:.1f}%)   "
            f"Total at-risk: {total_risk/capital_inr*100:.2f}% of capital"
        )

    skipped = _stocks_skipped_for_sizing(selected, positions)
    if skipped:
        print()
        print("  Selected but skipped at sizing step:")
        for s in skipped:
            note = "; ".join(s.notes) if s.notes else "(unknown)"
            print(f"    {s.symbol:<12}  ₹{s.last_close:,.0f}   reason: {note}")

    print()
    print("=" * 78)


