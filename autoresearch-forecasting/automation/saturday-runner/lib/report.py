def write_gtt_csv(out_path: Path, positions: list[Position]):
    """CSV format that you can review and either:
      (a) hand-enter into the broker's GTT UI, or
      (b) feed to a small uploader script using the broker's REST API.
    Columns are intentionally broker-agnostic; map them in your uploader.
    """
    fields = [
        "symbol",
        "isin",
        "side",
        "quantity",
        "trigger_price",
        "limit_price",
        "order_type",
        "validity",
        "tag",
    ]
    with out_path.open("w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fields)
        w.writeheader()
        for p in positions:
            limit_price = round(p.final_stop_price * 0.997, 2)
            w.writerow(
                {
                    "symbol": p.symbol,
                    "isin": p.isin,
                    "side": "SELL",
                    "quantity": p.shares,
                    "trigger_price": round(p.final_stop_price, 2),
                    "limit_price": limit_price,
                    "order_type": "SL-L",
                    "validity": "GTT",
                    "tag": "saturday-runner-stop",
                }
            )


